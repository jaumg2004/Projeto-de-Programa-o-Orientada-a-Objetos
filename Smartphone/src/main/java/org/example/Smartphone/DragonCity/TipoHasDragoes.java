package org.example.Smartphone.DragonCity;

public class TipoHasDragoes {

    private String tipo;
    private String dragoes;

    public TipoHasDragoes(String tipo, String dragoes) {
        this.tipo = tipo;
        this.dragoes = dragoes;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getDragoes() {
        return dragoes;
    }

    public void setDragoes(String dragoes) {
        this.dragoes = dragoes;
    }
}
